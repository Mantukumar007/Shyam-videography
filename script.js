function getLocale(lang) {
  return lang === "hi" ? "hi-IN" : "en-IN";
}

function applyMuhuratDates(data) {
  if (!DEFAULT_MUHURAT_DATES.length) {
    return data;
  }
  const updated = { ...data };
  DEFAULT_MUHURAT_DATES.forEach((date) => {
    if (!updated[date]) {
      updated[date] = { status: "shubh", details: "Vivah Muhurat" };
    }
  });
  return updated;
}

function getCalendarStatus(entry) {
  if (!entry) return "normal";
  const status = entry.status || entry.availability || "normal";
  if (status === "booked") return "booked";
  if (status === "shubh") return "shubh";
  if (status === "available") return "available";
  return "normal";
}

function loadCalendarData() {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    const data = raw ? JSON.parse(raw) : {};
    return applyMuhuratDates(data);
  } catch (error) {
    return {};
  }
}

function openWhatsAppLink(url) {
  const isMobile = /Android|iPhone|iPad|iPod/i.test(navigator.userAgent);
  if (isMobile) {
    window.location.href = url;
  } else {
    window.open(url, "_blank", "noopener");
  }
}

function loadBookings() {
  try {
    const raw = localStorage.getItem(BOOKINGS_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch (error) {
    return [];
  }
}

function saveBookings(bookings) {
  localStorage.setItem(BOOKINGS_KEY, JSON.stringify(bookings));
}

function getDateKey(date) {
  return date.toISOString().split("T")[0];
}

function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  const t = translations[lang];

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (t[key]) {
      el.textContent = t[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (t[key]) {
      el.setAttribute("placeholder", t[key]);
    }
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });

  if (formSuccess && formSuccess.textContent.trim().length > 0) {
    formSuccess.textContent = t.success_msg;
  }

  renderGallery(activeFilter);
  renderCalendar();
}

function renderGallery(filter) {
  if (!galleryGrid) return;
  galleryGrid.innerHTML = "";
  filteredItems = galleryItems.filter((item) => filter === "all" || item.category === filter);

  filteredItems.forEach((item, index) => {
    const card = document.createElement("div");
    card.className = "gallery-item";
    card.dataset.index = index;

    const img = document.createElement("img");
    img.src = item.src;
    img.alt = translations[currentLang][item.labelKey];
    img.loading = "lazy";
    img.decoding = "async";

    const badge = document.createElement("span");
    badge.className = "badge";
    badge.textContent = translations[currentLang][item.labelKey];

    card.appendChild(img);
    card.appendChild(badge);
    card.addEventListener("click", () => openLightbox(index));

    galleryGrid.appendChild(card);
  });
}

function openLightbox(index) {
  activeIndex = index;
  const item = filteredItems[activeIndex];
  if (!item || !lightbox) return;
  lightboxImage.src = item.src;
  lightboxCaption.textContent = translations[currentLang][item.labelKey];
  lightbox.classList.add("open");
  lightbox.setAttribute("aria-hidden", "false");
}

function closeLightbox() {
  if (!lightbox) return;
  lightbox.classList.remove("open");
  lightbox.setAttribute("aria-hidden", "true");
}

function nextLightbox(step) {
  activeIndex = (activeIndex + step + filteredItems.length) % filteredItems.length;
  openLightbox(activeIndex);
}

function openVideoLightbox(card) {
  const type = card.dataset.type;
  const src = card.dataset.src;
  const poster = card.dataset.poster;
  const title = card.dataset.title || card.querySelector("h3")?.textContent || "";

  if (!videoFrame || !videoLightbox) return;
  videoFrame.innerHTML = "";

  if (type === "youtube") {
    const iframe = document.createElement("iframe");
    iframe.src = src;
    iframe.title = title || "Wedding video";
    iframe.allow = "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture";
    iframe.allowFullscreen = true;
    videoFrame.appendChild(iframe);
  } else {
    const video = document.createElement("video");
    video.controls = true;
    video.playsInline = true;
    if (poster) {
      video.poster = poster;
    }
    const source = document.createElement("source");
    source.src = src;
    source.type = "video/mp4";
    video.appendChild(source);
    videoFrame.appendChild(video);
  }

  videoCaption.textContent = title;
  videoLightbox.classList.add("open");
  videoLightbox.setAttribute("aria-hidden", "false");
}

function closeVideoLightbox() {
  if (!videoLightbox || !videoFrame) return;
  videoFrame.innerHTML = "";
  videoLightbox.classList.remove("open");
  videoLightbox.setAttribute("aria-hidden", "true");
}

function renderWeekdays(locale) {
  if (!calendarWeekdays) return;
  calendarWeekdays.innerHTML = "";
  const base = new Date(Date.UTC(2024, 0, 7));
  for (let i = 0; i < 7; i += 1) {
    const day = new Date(base.getTime() + i * 86400000);
    const label = day.toLocaleDateString(locale, { weekday: "short" });
    const span = document.createElement("span");
    span.textContent = label;
    calendarWeekdays.appendChild(span);
  }
}

function renderCalendar() {
  if (!calendarGrid || !calendarTitle) return;
  const locale = getLocale(currentLang);
  const data = loadCalendarData();
  calendarGrid.innerHTML = "";

  const monthStart = new Date(calendarMonth.getFullYear(), calendarMonth.getMonth(), 1);
  const monthEnd = new Date(calendarMonth.getFullYear(), calendarMonth.getMonth() + 1, 0);
  const startDay = monthStart.getDay();
  const totalDays = monthEnd.getDate();

  calendarTitle.textContent = monthStart.toLocaleDateString(locale, { month: "long", year: "numeric" });
  renderWeekdays(locale);

  for (let i = 0; i < startDay; i += 1) {
    const empty = document.createElement("div");
    empty.className = "calendar-day is-muted";
    calendarGrid.appendChild(empty);
  }

  const todayKey = getDateKey(new Date());

  for (let day = 1; day <= totalDays; day += 1) {
    const date = new Date(calendarMonth.getFullYear(), calendarMonth.getMonth(), day);
    const key = getDateKey(date);
    const cell = document.createElement("button");
    cell.type = "button";
    cell.className = "calendar-day";
    cell.textContent = String(day);

    const entry = data[key];
    const status = getCalendarStatus(entry);
    const isPast = key < todayKey;

    if (isPast) {
      cell.classList.add("is-muted");
      cell.disabled = true;
    } else if (status === "booked") {
      cell.classList.add("is-booked");
      cell.disabled = true;
    } else {
      if (status === "shubh") {
        cell.classList.add("is-shubh");
      } else if (status === "available") {
        cell.classList.add("is-available");
      } else {
        cell.classList.add("is-normal");
      }
      cell.addEventListener("click", () => selectCalendarDate(key, entry));
    }

    if (selectedDate === key) {
      cell.classList.add("is-selected");
    }

    calendarGrid.appendChild(cell);
  }

  updateCalendarNote(selectedDate, data[selectedDate]);
}

function selectCalendarDate(key, entry) {
  selectedDate = key;
  if (selectedDateInput) {
    selectedDateInput.value = key;
  }
  updateCalendarNote(key, entry);
  renderCalendar();
}

function updateCalendarNote(key, entry) {
  if (!calendarNote) return;
  if (!key) {
    calendarNote.textContent = translations[currentLang].cal_select_prompt;
    return;
  }

  const status = getCalendarStatus(entry);

  if (status === "booked") {
    calendarNote.textContent = entry?.details
      ? `${translations[currentLang].cal_booked_note} ${entry.details}`
      : translations[currentLang].cal_booked_note;
    return;
  }

  if (status === "shubh") {
    calendarNote.textContent = translations[currentLang].cal_shubh;
    return;
  }

  if (status === "available") {
    calendarNote.textContent = translations[currentLang].cal_available;
    return;
  }

  calendarNote.textContent = translations[currentLang].cal_normal;
}

function focusCalendarPanel() {
  if (!calendarPanel) return;
  calendarPanel.classList.add("is-highlight");
  calendarPanel.scrollIntoView({ behavior: "smooth", block: "center" });
  window.setTimeout(() => calendarPanel.classList.remove("is-highlight"), 1200);
}

